/**
 * Get compatibility description for two archetypes
 * @param userArchetype The user's primary archetype
 * @param partnerArchetype The partner archetype to describe compatibility with
 * @returns A ~50 word description of why they're compatible, or a default message
 */
export function getCompatibilityDescription(userArchetype: string, partnerArchetype: string): string {
  const userMap = ARCHETYPE_COMPATIBILITY_DESCRIPTIONS[userArchetype];
  if (userMap && userMap[partnerArchetype]) {
    return userMap[partnerArchetype];
  }
  
  return `${partnerArchetype}与你在社交风格上互补，能带来不一样的社交体验和视角。`;
}
